% -------- helpers --------
function out = char_from(v)
if iscell(v), out = char(v{1});
elseif isstring(v), out = char(v);
elseif isnumeric(v)
    if isnan(v), out = ''; else, out = sprintf('%g', v); end
else
    out = char(v);
end
if strcmpi(out, 'NaN'), out = ''; end
end

function out = num_str(v, fmt)
if isnumeric(v)
    if isnan(v), out = '--'; else, out = sprintf(fmt, v); end
else
    out = char_from(v);
    if isempty(out), out = '--'; end
end
end

function out = fmt_num_1dp(v)
out = num_str(v, '%.1f');
end

function out = fmt_num_2dp(v)
out = num_str(v, '%.2f');
end

function out = fillfwd(c)
last = '';
out = c;
for i = 1:numel(c)
    s = c{i};
    if isempty(s) || all(isspace(s))
        out{i} = last;
    else
        out{i} = s;
        last = s;
    end
end
end

function out = uniq_preserve(c)
seen = containers.Map('KeyType', 'char', 'ValueType', 'logical');
out = {};
for i = 1:numel(c)
    s = c{i};
    if ~isKey(seen, s)
        seen(s) = true;
        out{end+1} = s; %#ok<AGROW>
    end
end
end
