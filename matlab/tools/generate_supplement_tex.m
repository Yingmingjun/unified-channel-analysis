function generate_supplement_tex(target_path)
% generate_supplement_tex  Build standalone IEEE TWC supplement with full per-link tables.
%
%   Matches main_final.tex table formatting verbatim:
%     * N1 @ 142 GHz layout (tab:LSPs)   -> p{0.6 cm}... column spec
%     * U3 and N3 cross-processing       -> full \columncolor{gray!20} on
%                                           "orig.\ (U1/N1)" columns and
%                                           !{\vrule width 1.2pt} separators
%
%   Reads values from figures/matlab/table{04,05,08,09,10,11}.csv.
%
%   Target path is resolved from:
%     1. Explicit target_path arg (if provided)
%     2. paths().paper_src_supp_path (env var PAPER_SUPP_PATH or
%        PAPER_TREE_DIR/supplement.tex)
%     3. Falls back to figures/matlab/supplement.tex (local; does NOT
%        overwrite the paper tree).

U = paths();
csv_dir = U.out_dir;
if nargin < 1 || isempty(target_path)
    if ~isempty(U.paper_src_supp_path)
        target_path = U.paper_src_supp_path;
    else
        target_path = fullfile(csv_dir, 'supplement.tex');
        fprintf('generate_supplement_tex: PAPER_SUPP_PATH not set; writing locally to %s\n', target_path);
    end
end

blocks = {};
blocks{end+1} = header_block();

% N1 and U1 standalone tables are intentionally omitted. The N1 values are
% already embedded in the N3 tables as the ``NYU orig. (N1)'' column, and
% U1 values are embedded in U3 tables as the ``USC orig. (U1)'' column.
% Listing them standalone would duplicate content.

blocks{end+1} = cross_section( ...
    fullfile(csv_dir, 'table08_U3_145.csv'), ...
    '145', 'U3_145_full', ...
    {'NYU thres', 'USC thres', 'USC orig.\ (U1)'}, ...
    'Full \textbf{U3 @ 145.5 GHz}: Point data table for USC data processed using NYU-replicated USC processing methods with both NYU''s max(25 dB below local PDP peak, 5 dB above noise floor) delay-domain threshold w/ 10 dB SLT and USC''s 12 dB above global max noise floor threshold.', ...
    'USC replicated NYU processing methods, USC Data \textbf{(U3) @ 145.5 GHz}', ...
    'u3_145');

blocks{end+1} = cross_section( ...
    fullfile(csv_dir, 'table09_U3_7.csv'), ...
    '6.75', 'U3_7_full', ...
    {'NYU thres', 'USC thres', 'USC orig.\ (U1)'}, ...
    'Full \textbf{U3 @ 6.75 GHz}: Point data table for USC data processed using NYU-replicated USC processing methods. Column definitions identical to Table~\ref{tab:U3_145_full}.', ...
    'USC replicated NYU processing methods, USC Data \textbf{(U3) @ 6.75 GHz}', ...
    'u3_7');

blocks{end+1} = cross_section( ...
    fullfile(csv_dir, 'table10_N3_142.csv'), ...
    '142', 'N3_142_full', ...
    {'USC thres', 'NYU thres', 'NYU orig.\ (N1)'}, ...
    'Full \textbf{N3 @ 142 GHz}: Point data table for NYU data processed using USC-replicated NYU processing methods.', ...
    'NYU replicated USC processing methods, NYU Data \textbf{(N3) @ 142 GHz}', ...
    'n3_142');

blocks{end+1} = cross_section( ...
    fullfile(csv_dir, 'table11_N3_7.csv'), ...
    '6.75', 'N3_7_full', ...
    {'USC thres', 'NYU thres', 'NYU orig.\ (N1)'}, ...
    'Full \textbf{N3 @ 6.75 GHz}: Point data table for NYU data processed using USC-replicated NYU processing methods. Column definitions identical to Table~\ref{tab:N3_142_full}.', ...
    'NYU replicated USC processing methods, NYU Data \textbf{(N3) @ 6.75 GHz}', ...
    'n3_7');

blocks{end+1} = footer_block();

fid = fopen(target_path, 'w');
if fid == -1, error('Cannot open %s', target_path); end
fprintf(fid, '%s', strjoin(blocks, sprintf('\n\n')));
fclose(fid);
fprintf('generate_supplement_tex: wrote %s\n', target_path);
end

